		}

		internal override uint TextToVK(
			ReadOnlySpan<char> text,
			ref uint? modifiersLr,
			bool excludeThoseHandledByScanCode,
			bool allowExplicitVK,
			nint keybdLayout)
		{
			if (text.Length == 0)
				return 0;

			// 1) Single character → ask the layout-aware mapper first.
			//    It returns the base VK (e.g., '1', 'Q', VK_OEM_*), plus whether Shift/AltGr are needed.
			if (text.Length == 1)
			{
				var rune = new Rune(text[0]);
				if (LinuxKeyboardMouseSender.LinuxCharMapper.TryMapRuneToKeystroke(rune, out var vk, out var needShift, out var needAltGr))
				{
					if (vk != 0)
					{
						uint mods = modifiersLr ?? 0;
						if (needShift) mods |= MOD_LSHIFT;
						if (needAltGr) mods |= MOD_RALT;   // AltGr usually maps to Right-Alt on Linux
						modifiersLr = mods;
						return vk;
					}
				}

				// Fallbacks for common ASCII if mapper couldn’t resolve:
				char ch = text[0];
				if (char.IsLetter(ch))
				{
					bool upper = char.IsUpper(ch);
					uint mods = modifiersLr ?? 0;
					if (upper) mods |= MOD_LSHIFT;
					modifiersLr = mods;
					return (uint)char.ToUpperInvariant(ch);
				}
				if (char.IsDigit(ch))
					return (uint)ch;

				switch (ch)
				{
					case ' ': return VK_SPACE;
					case '\t': return VK_TAB;
					case '\r':
					case '\n': return VK_RETURN;
				}
			}

			// 2) Multi-char tokens, names, function keys, numpad, explicit vk/sc.
			//    Normalize: trim + lowercase ASCII; users may pass "Enter", "escape", "PgDn", etc.
			var name = text.ToString().Trim();
			if (name.Length == 0)
				return 0;

			// Allow explicit VK (e.g. "vk1B", "vk25") when permitted.
			// Accept "vk" + 1..4 hex digits. (AHK accepts hex for VK.)
			if (allowExplicitVK && (name.Length >= 3) && (name[0] == 'v' || name[0] == 'V') && (name[1] == 'k' || name[1] == 'K'))
			{
				var hex = name.AsSpan(2);
				if (hex.Length is >= 1 and <= 4)
				{
					if (uint.TryParse(hex, System.Globalization.NumberStyles.HexNumber, null, out var vk))
						return vk;
				}
			}

			// If it looks like a scan code "scNNN", prefer the SC path when asked to exclude.
			// (On Linux MapScToVk is typically 0; keeping contract consistent with Windows.)
			if ((name.Length >= 3) && (name[0] == 's' || name[0] == 'S') && (name[1] == 'c' || name[1] == 'C'))
			{
				if (excludeThoseHandledByScanCode)
					return 0;

				// Try parse decimal or hex after "sc" (AHK often uses hex). If you later implement MapScToVk, use it here.
				var num = name.AsSpan(2).Trim();
				// accept hex like "159" or "0x159" or "159h"
				string ns = num.ToString();
				if (ns.StartsWith("0x", StringComparison.OrdinalIgnoreCase))
					ns = ns[2..];
				if (ns.EndsWith("h", StringComparison.OrdinalIgnoreCase))
					ns = ns[..^1];

				if (uint.TryParse(ns, System.Globalization.NumberStyles.HexNumber, null, out var sc))
				{
					uint vkFromSc = MapScToVk(sc);
					if (vkFromSc != 0)
						return vkFromSc;
				}
				return 0;
			}

			// Lowercase for name matching
			var n = name.ToLowerInvariant();

			// Function keys F1..F24
			if ((n.Length >= 2) && (n[0] == 'f') && int.TryParse(n.AsSpan(1), out var fnum) && fnum >= 1 && fnum <= 24)
			{
				return VK_F1 + (uint)(fnum - 1);
			}

			// Common names & synonyms
			switch (n)
			{
				// main editing / navigation
				case "enter":
				case "return":
					return VK_RETURN;

				case "tab":
					return VK_TAB;

				case "esc":
				case "escape":
					return VK_ESCAPE;

				case "space":
				case "spacebar":
					return VK_SPACE;

