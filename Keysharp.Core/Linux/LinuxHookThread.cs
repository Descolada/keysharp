		internal override uint TextToSpecial(ReadOnlySpan<char> text, ref KeyEventTypes eventType, ref uint modifiersLr, bool updatePersistent)
		{
			// Reuse Windows parsing for basic modifier down/up tokens.
			// We support SHIFT/CTRL/ALT/WIN down/up; otherwise return 0.
			var t = text.ToString();
			if (t.StartsWith("SHIFTDOWN", StringComparison.OrdinalIgnoreCase)) { eventType = KeyEventTypes.KeyDown; return VK_SHIFT; }
			if (t.StartsWith("SHIFTUP", StringComparison.OrdinalIgnoreCase)) { eventType = KeyEventTypes.KeyUp; return VK_SHIFT; }
			if (t.StartsWith("CTRLDOWN", StringComparison.OrdinalIgnoreCase) || t.StartsWith("CONTROLDOWN", StringComparison.OrdinalIgnoreCase)) { eventType = KeyEventTypes.KeyDown; return VK_CONTROL; }
			if (t.StartsWith("CTRLUP", StringComparison.OrdinalIgnoreCase) || t.StartsWith("CONTROLUP", StringComparison.OrdinalIgnoreCase)) { eventType = KeyEventTypes.KeyUp; return VK_CONTROL; }
