				case "ins":
					return VK_INSERT;

				case "home":
					return VK_HOME;

				case "end":
					return VK_END;

				case "pgup":
				case "pageup":
					return VK_PRIOR;

				case "pgdn":
				case "pagedown":
					return VK_NEXT;

				case "left":
					return VK_LEFT;
				case "right":
					return VK_RIGHT;
				case "up":
					return VK_UP;
				case "down":
					return VK_DOWN;

				// lock keys / system
				case "capslock":
					return VK_CAPITAL;
				case "scrolllock":
					return VK_SCROLL;
				case "numlock":
					return VK_NUMLOCK;
				case "printscreen":
