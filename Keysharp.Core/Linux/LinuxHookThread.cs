		{
			if (HasKbdHook() && vk < logicalKeyState.Length)
				return logicalKeyState[vk];

			// Fallback: query X11 logical state for any key.
			if (TryQueryKeyState(vk, out var isDown))
				return isDown;

			return false;
		}
